#include "BinaryOperators.h"

namespace webwork::expression {
    double Add(double lhs, double rhs) {
        return lhs + rhs;
    }

    double Subtract(double lhs, double rhs) {
        return lhs - rhs;
    }

    double Multiply(double lhs, double rhs) {
        return lhs * rhs;
    }

    double Divide(double lhs, double rhs) {
        return lhs / rhs;
    }
}